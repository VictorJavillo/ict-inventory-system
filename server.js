app.put("/api/inventory/:id", requireRole("admin", "staff"), async (req, res) => {
  const { id } = req.params;

  try {
    const payload = sanitizeInventoryPayload(req.body);

    const existingResult = await pool.query(
      `SELECT * FROM inventory WHERE id = $1 LIMIT 1`,
      [id]
    );

    const existingRow = existingResult.rows[0];

    if (!existingRow) {
      return res.status(404).json({ error: "Record not found." });
    }

    const finalNr = payload.nr !== "" ? payload.nr : existingRow.nr || "";

    const updateResult = await pool.query(
      `UPDATE inventory SET
        nr = $1,
        category = $2,
        description = $3,
        serial_number = $4,
        property_number = $5,
        status = $6,
        date_issued = $7,
        unit = $8,
        os = $9,
        windows_type = $10,
        ms_office = $11,
        antivirus = $12,
        remarks = $13
      WHERE id = $14
      RETURNING id`,
      [
        finalNr,
        payload.category,
        payload.description,
        payload.serial_number,
        payload.property_number,
        payload.status,
        payload.date_issued,
        payload.unit,
        payload.os,
        payload.windows_type,
        payload.ms_office,
        payload.antivirus,
        payload.remarks,
        id
      ]
    );

    if (updateResult.rows.length === 0) {
      return res.status(404).json({ error: "Record not found." });
    }

    res.json({
      success: true,
      message: "Inventory updated successfully.",
      nr: finalNr
    });
  } catch (error) {
    console.error("Update inventory error:", error.message);
    res.status(500).json({ error: error.message });
  }
});

