// admin only: create user
app.post("/api/users", requireRole("admin"), async (req, res) => {
  try {
    const username = normalizeText(req.body.username);
    const password = String(req.body.password || "").trim();
    const role = normalizeText(req.body.role).toLowerCase() || "viewer";

    if (!username || !password) {
      return res.status(400).json({ error: "Username and password are required." });
    }

    if (!isValidRole(role)) {
      return res.status(400).json({ error: "Invalid role." });
    }

    const existing = await pool.query(
      `SELECT id FROM users WHERE username = $1 LIMIT 1`,
      [username]
    );

    if (existing.rows.length > 0) {
      return res.status(409).json({ error: "Username already exists." });
    }

    const hashedPassword = await bcrypt.hash(password, 10);

    const result = await pool.query(
      `INSERT INTO users (username, password, role)
       VALUES ($1, $2, $3)
       RETURNING id, username, role`,
      [username, hashedPassword, role]
    );

    res.json({
      success: true,
      user: result.rows[0]
    });
  } catch (error) {
    console.error("Create user error:", error.message);
    res.status(500).json({ error: error.message });
  }
});

// admin only: update user role and optional password
app.put("/api/users/:id", requireRole("admin"), async (req, res) => {
  try {
    const { id } = req.params;
    const role = normalizeText(req.body.role).toLowerCase();
    const newPassword = String(req.body.password || "").trim();

    const existing = await pool.query(
      `SELECT id, username, role FROM users WHERE id = $1 LIMIT 1`,
      [id]
    );

    if (existing.rows.length === 0) {
      return res.status(404).json({ error: "User not found." });
    }

    const user = existing.rows[0];

    if (role && !isValidRole(role)) {
      return res.status(400).json({ error: "Invalid role." });
    }

    if (user.username === "admin" && role === "viewer") {
      return res.status(400).json({ error: "Main admin cannot be downgraded to viewer." });
    }

    if (role && newPassword) {
      const hashedPassword = await bcrypt.hash(newPassword, 10);

      await pool.query(
        `UPDATE users SET role = $1, password = $2 WHERE id = $3`,
        [role, hashedPassword, id]
      );
    } else if (role) {
      await pool.query(
        `UPDATE users SET role = $1 WHERE id = $2`,
        [role, id]
      );
    } else if (newPassword) {
      const hashedPassword = await bcrypt.hash(newPassword, 10);

      await pool.query(
        `UPDATE users SET password = $1 WHERE id = $2`,
        [hashedPassword, id]
      );
    } else {
      return res.status(400).json({ error: "Nothing to update." });
    }

    const updated = await pool.query(
      `SELECT id, username, role FROM users WHERE id = $1`,
      [id]
    );

    res.json({
      success: true,
      user: updated.rows[0]
    });
  } catch (error) {
    console.error("Update user error:", error.message);
    res.status(500).json({ error: error.message });
  }
});

